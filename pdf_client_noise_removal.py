# =====================================================================
# CELL 3: THE CLEANING ALGORITHM (With Individual Edge Controls)
# =====================================================================
# You can fine-tune individual sides (Top, Bottom, Left, Right) here.
# To protect headers/footers, reduce top and bottom values.
# =====================================================================

def clean_page_image(img, block_size=31, c_val=15, bilateral_d=9,

                     # ─── 1. ABSOLUTE BORDER CROP (Forces edge strips to pure white) ───
                     margin_top_pct    = 0.000,  # Set to 0.0 to completely protect top headers
                     margin_bottom_pct = 0.000,  # Set to 0.0 to completely protect bottom footers
                     margin_left_pct   = 0.010,  # Left side crop (2%)
                     margin_right_pct  = 0.026,  # Right side crop (2%)

                     # ─── 2. SMART CLEANUP ZONES (Inspects outer margins for scanning noise) ───
                     smart_top_pct    = 0.020,  # Only inspect top 2% (reduced to protect headers)
                     smart_bottom_pct = 0.020,  # Only inspect bottom 2% (reduced to protect page numbers)
                     smart_left_pct   = 0.050,  # Inspect outer 10% of left side
                     smart_right_pct  = 0.065   # Inspect outer 10% of right side
                    ):
    """
    Cleans a page image with independent margin controls for each edge.
    """
    h, w = img.shape[:2]

    # 1. Convert to grayscale
    gray = cv2.cvtColor(img, cv2.COLOR_BGR2GRAY)

    # 2. Apply bilateral filter to smooth paper noise but keep text edges sharp
    if bilateral_d > 0:
        smoothed = cv2.bilateralFilter(gray, d=bilateral_d, sigmaColor=75, sigmaSpace=75)
    else:
        smoothed = gray

    # 3. Adaptive thresholding
    thresh = cv2.adaptiveThreshold(
        smoothed,
        255,
        cv2.ADAPTIVE_THRESH_GAUSSIAN_C,
        cv2.THRESH_BINARY,
        block_size,
        c_val
    )

    # 4. Clean Borders and Edge Shadows
    # 4a. Clear absolute outer margins (with individual top/bottom/left/right controls)
    my_top    = int(h * margin_top_pct)
    my_bottom = int(h * margin_bottom_pct)
    mx_left   = int(w * margin_left_pct)
    mx_right  = int(w * margin_right_pct)

    if my_top > 0:
        thresh[0:my_top, :] = 255
    if my_bottom > 0:
        thresh[h-my_bottom:h, :] = 255
    if mx_left > 0:
        thresh[:, 0:mx_left] = 255
    if mx_right > 0:
        thresh[:, w-mx_right:w] = 255

    # 4b. Smart edge shadow removal using connected components
    inverted = cv2.bitwise_not(thresh)
    num_labels, labels, stats, centroids = cv2.connectedComponentsWithStats(inverted, connectivity=8)

    # Define margin boundaries based on individual side percentages
    bx_left   = int(w * smart_left_pct)
    bx_right  = int(w * smart_right_pct)
    by_top    = int(h * smart_top_pct)
    by_bottom = int(h * smart_bottom_pct)

    for label in range(1, num_labels):
        left = stats[label, cv2.CC_STAT_LEFT]
        top = stats[label, cv2.CC_STAT_TOP]
        width = stats[label, cv2.CC_STAT_WIDTH]
        height = stats[label, cv2.CC_STAT_HEIGHT]
        area = stats[label, cv2.CC_STAT_AREA]

        right = left + width
        bottom = top + height

        # 1. Check if the component is COMPLETELY within the outer margins
        is_in_left_margin   = right < bx_left
        is_in_right_margin  = left > (w - bx_right)
        is_in_top_margin    = bottom < by_top
        is_in_bottom_margin = top > (h - by_bottom)

        if is_in_left_margin or is_in_right_margin or is_in_top_margin or is_in_bottom_margin:
            # Erase it if it matches small/medium noise sizes
            if area < (w * h) * 0.02 or (width < w * 0.08 and height < h * 0.08):
                thresh[labels == label] = 255
                continue

        # 2. Check if the component touches or is very close to the edge
        near_left   = left < bx_left
        near_right  = right > (w - bx_right)
        near_top    = top < by_top
        near_bottom = bottom > (h - by_bottom)

        if near_left or near_right or near_top or near_bottom:
            touches_edge = (left <= 2) or (right >= w - 2) or (top <= 2) or (bottom >= h - 2)

            is_noise = False
            if touches_edge:
                # If touching boundary, erase if it is medium size or larger
                if width > w * 0.03 or height > h * 0.03 or area > 300:
                    is_noise = True
            else:
                # Near the edge but not touching - erase large spine/gutter shadow blobs
                if area > 1000 or width > w * 0.06 or height > h * 0.06:
                    is_noise = True

            if is_noise:
                thresh[labels == label] = 255

    return thresh

print("✅ Updated cleaning algorithm with individual side controls defined!")

# =====================================================================
# CELL 4: THE PDF PIPELINE
# =====================================================================
# Reads the original PDF, processes each page, and builds output PDF.
# =====================================================================

def clean_pdf(input_path, output_path, start_page=1, end_page=None, dpi=300):
    print(f"📖 Opening: {input_path}")
    doc = fitz.open(input_path)
    total_pages = len(doc)

    start_idx = max(0, start_page - 1)
    end_idx = min(total_pages, end_page if end_page is not None else total_pages)
    num_pages = end_idx - start_idx

    print(f"   Total pages in PDF: {total_pages}")
    print(f"   Processing: page {start_idx + 1} to {end_idx} ({num_pages} pages)")
    print(f"   Resolution: {dpi} DPI")
    print("─" * 50)

    out_doc = fitz.open()
    zoom = dpi / 72.0
    matrix = fitz.Matrix(zoom, zoom)
    t_start = time.time()

    for idx in range(start_idx, end_idx):
        page_num = idx + 1
        t_page = time.time()

        page = doc[idx]
        pix = page.get_pixmap(matrix=matrix)
        img = np.frombuffer(pix.samples, dtype=np.uint8).reshape(pix.h, pix.w, pix.n)

        if pix.n == 3:
            img = cv2.cvtColor(img, cv2.COLOR_RGB2BGR)
        elif pix.n == 4:
            img = cv2.cvtColor(img, cv2.COLOR_RGBA2BGRA)
        elif pix.n == 1:
            img = cv2.cvtColor(img, cv2.COLOR_GRAY2BGR)

        # Clean using Cell 3 function
        cleaned = clean_page_image(img)

        # Encode as PNG to preserve exact lossless quality
        ok, buf = cv2.imencode(".png", cleaned)
        if not ok:
            print(f"   ⚠️  Page {page_num}: encoding failed, skipping")
            continue

        rect = page.rect
        out_page = out_doc.new_page(width=rect.width, height=rect.height)
        out_page.insert_image(rect, stream=buf.tobytes())

        print(f"   ✅ Page {page_num}/{total_pages} done ({time.time()-t_page:.2f}s)")

    print("─" * 50)
    print(f"💾 Saving cleaned PDF to: {output_path}")

    out_doc.save(output_path, garbage=3, deflate=True)
    out_doc.close()
    doc.close()

    total_time = time.time() - t_start
    in_size = os.path.getsize(input_path) / (1024 * 1024)
    out_size = os.path.getsize(output_path) / (1024 * 1024)
    reduction = (1 - out_size / in_size) * 100

    print()
    print("═" * 50)
    print("📊 SUMMARY")
    print("═" * 50)
    print(f"   Pages processed:  {num_pages}")
    print(f"   Total time:       {total_time:.1f} seconds")
    print(f"   Speed:            {num_pages / total_time:.1f} pages/second")
    print(f"   Input file size:  {in_size:.2f} MB")
    print(f"   Output file size: {out_size:.2f} MB")
    print(f"   Compression:      {reduction:.1f}% reduction")
    print("═" * 50)

print("✅ PDF pipeline defined successfully!")

