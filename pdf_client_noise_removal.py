# ─── 2. CONFIGURE FILE NAMES ───
INPUT_FILENAME      = "SC0496.PDF"                # Your noisy original PDF
OUTPUT_FILENAME     = "SC0496_Cleanest.pdf"       # Your cleaned PDF
COMPARISON_FILENAME = "SC0496_Comparison_Cleanest.pdf" # Output comparison PDF

# ─── 3. RESOLUTION SETTINGS ───
# 150/200 DPI keeps file sizes small and downloads fast.
# Change to 300 DPI if you want absolute max print-quality.
COMPARE_DPI = 300

# ─── 4. PATH RESOLUTION (NameError Fix) ───
CURRENT_FOLDER = os.getcwd()
INPUT_PDF      = os.path.join(CURRENT_FOLDER, INPUT_FILENAME)
OUTPUT_PDF     = os.path.join(CURRENT_FOLDER, OUTPUT_FILENAME)
COMPARISON_PDF = os.path.join(CURRENT_FOLDER, COMPARISON_FILENAME)

# ─── 5. GENERATION PROCESS ───
# Verify files exist before opening
if not os.path.exists(INPUT_PDF):
    raise FileNotFoundError(f"Original PDF not found at: {INPUT_PDF}")
if not os.path.exists(OUTPUT_PDF):
    raise FileNotFoundError(f"Cleaned PDF not found at: {OUTPUT_PDF}. Please run Cell 5 first to clean your PDF.")

print(f"📖 Opening: {INPUT_PDF}")
doc_orig = fitz.open(INPUT_PDF)
doc_clean = fitz.open(OUTPUT_PDF)

total_orig = len(doc_orig)
total_clean = len(doc_clean)
total_pages = min(total_orig, total_clean)

# Calculate page range index (0-indexed)
start_idx = max(0, START_PAGE_COMP - 1)
end_idx = min(total_pages, END_PAGE_COMP if END_PAGE_COMP is not None else total_pages)
num_pages = end_idx - start_idx

print(f"   Original PDF: {total_orig} pages")
print(f"   Cleaned PDF:  {total_clean} pages")
print(f"   Creating comparison PDF for pages {start_idx+1} to {end_idx} ({num_pages} pages)...")
print("─" * 50)

# Create a blank PDF for side-by-side pages
doc_compare = fitz.open()

zoom = COMPARE_DPI / 72.0
matrix = fitz.Matrix(zoom, zoom)
t_start = time.time()

for idx in range(start_idx, end_idx):
    page_num = idx + 1
    t_page = time.time()

    # Render the original page (Keep original color/RGB)
    page_o = doc_orig[idx]
    pix_o = page_o.get_pixmap(matrix=matrix)
    img_orig = np.frombuffer(pix_o.samples, dtype=np.uint8).reshape(pix_o.h, pix_o.w, pix_o.n)

    if pix_o.n == 3:
        img_orig = cv2.cvtColor(img_orig, cv2.COLOR_RGB2BGR)
    elif pix_o.n == 4:
        img_orig = cv2.cvtColor(img_orig, cv2.COLOR_RGBA2BGR)
    elif pix_o.n == 1:
        img_orig = cv2.cvtColor(img_orig, cv2.COLOR_GRAY2BGR)

    # Render the cleaned page (Keep original color/RGB)
    page_c = doc_clean[idx]
    pix_c = page_c.get_pixmap(matrix=matrix)
    img_clean = np.frombuffer(pix_c.samples, dtype=np.uint8).reshape(pix_c.h, pix_c.w, pix_c.n)

    if pix_c.n == 3:
        img_clean = cv2.cvtColor(img_clean, cv2.COLOR_RGB2BGR)
    elif pix_c.n == 4:
        img_clean = cv2.cvtColor(img_clean, cv2.COLOR_RGBA2BGR)
    elif pix_c.n == 1:
        img_clean = cv2.cvtColor(img_clean, cv2.COLOR_GRAY2BGR)

    # Match heights if they differ slightly
    h_orig, w_orig = img_orig.shape[:2]
    h_clean, w_clean = img_clean.shape[:2]
    if h_orig != h_clean:
        new_w = int(w_clean * h_orig / h_clean)
        img_clean = cv2.resize(img_clean, (new_w, h_orig), interpolation=cv2.INTER_CUBIC)

    # Combine side-by-side (Original on left, Cleaned on right)
    combined = np.hstack((img_orig, img_clean))

    # Encode as lossless PNG
    ok, buf = cv2.imencode(".png", combined)
    if not ok:
        print(f"   ⚠️ Page {page_num}: failed to encode comparison")
        continue

    # Calculate page dimensions in PDF points
    combined_h, combined_w = combined.shape[:2]
    page_w_pts = combined_w * 72.0 / COMPARE_DPI
    page_h_pts = combined_h * 72.0 / COMPARE_DPI

    # Create comparison page and insert the side-by-side image
    compare_page = doc_compare.new_page(width=page_w_pts, height=page_h_pts)
    target_rect = fitz.Rect(0, 0, page_w_pts, page_h_pts)
    compare_page.insert_image(target_rect, stream=buf.tobytes())

    print(f"   ✅ Page {page_num}/{total_pages} comparison created ({time.time()-t_page:.2f}s)")

# Save the final comparison PDF
print("─" * 50)
print(f"💾 Saving comparison PDF to: {COMPARISON_PDF}")
doc_compare.save(COMPARISON_PDF, garbage=3, deflate=True)
doc_compare.close()
doc_orig.close()
doc_clean.close()

elapsed = time.time() - t_start
file_size = os.path.getsize(COMPARISON_PDF) / (1024 * 1024)
print(f"🎉 Success! Comparison PDF saved as '{COMPARISON_FILENAME}' ({file_size:.2f} MB) in {elapsed:.1f}s.")









